---- DROP PROCEDURE SP_FETCH_IFRS_MASTER_RESTRU;

CREATE OR REPLACE PROCEDURE SP_FETCH_IFRS_MASTER_RESTRU(
    IN P_RUNID VARCHAR(20) DEFAULT 'S_00000_0000',
    IN P_DOWNLOAD_DATE DATE DEFAULT NULL,
    IN P_PRC VARCHAR(1) DEFAULT 'S')
LANGUAGE PLPGSQL AS $$
DECLARE
    ---- DATE
    V_PREVDATE DATE;
    V_CURRDATE DATE;

    ---- QUERY   
    V_STR_QUERY TEXT;

    ---- TABLE LIST       
    V_TABLEINSERT1 VARCHAR(100);
    V_TABLEINSERT2 VARCHAR(100);
    
    ---- CONDITION
    V_RETURNROWS INT;
    V_RETURNROWS2 INT;
    V_TABLEDEST VARCHAR(100);
    V_COLUMNDEST VARCHAR(100);
    V_SPNAME VARCHAR(100);
    V_OPERATION VARCHAR(100);

    ---- RESULT
    V_QUERYS TEXT;

    --- VARIABLE
    V_SP_NAME VARCHAR(100);
    STACK TEXT; 
    FCESIG TEXT;
BEGIN 
    -------- ====== VARIABLE ======
	GET DIAGNOSTICS STACK = PG_CONTEXT;
	FCESIG := substring(STACK from 'function (.*?) line');
	V_SP_NAME := UPPER(LEFT(fcesig::regprocedure::text, POSITION('(' in fcesig::regprocedure::text)-1));

    IF COALESCE(P_PRC, NULL) IS NULL THEN
        P_PRC := 'S';
    END IF;

    IF COALESCE(P_RUNID, NULL) IS NULL THEN
        P_RUNID := 'S_00000_0000';
    END IF;

    IF P_PRC = 'S' THEN 
        V_TABLEINSERT1 := 'IFRS_MASTER_RESTRU_SIFAT_' || P_RUNID || '';
        V_TABLEINSERT2 := 'IFRS_MASTER_RESTRU_COLLECT_' || P_RUNID || '';
    ELSE 
        V_TABLEINSERT1 := 'IFRS_MASTER_RESTRU_SIFAT';
        V_TABLEINSERT2 := 'IFRS_MASTER_RESTRU_COLLECT';
    END IF;
    
    IF P_DOWNLOAD_DATE IS NULL 
    THEN
        SELECT
            CURRDATE, PREVDATE INTO V_CURRDATE, V_PREVDATE
        FROM
            DBLINK('ifrs9_stg', 'SELECT CURRDATE, PREVDATE FROM STG_PRC_DATE') AS STG_PRC_DATE(CURRDATE DATE, PREVDATE DATE);
    ELSE        
        V_CURRDATE := P_DOWNLOAD_DATE;
        V_PREVDATE := V_CURRDATE - INTERVAL '1 DAY';
    END IF;

    IF V_CURRDATE IS NULL THEN 
        SELECT CURRDATE INTO V_CURRDATE 
        FROM IFRS_PRC_DATE;
    END IF;

    IF V_PREVDATE IS NULL THEN 
        SELECT PREVDATE INTO V_PREVDATE 
        FROM IFRS_PRC_DATE; 
    END IF;
    
    V_RETURNROWS2 := 0;
    -------- ====== VARIABLE ======

    -------- ====== PRE SIMULATION TABLE ======
    IF P_PRC = 'S' THEN
        V_STR_QUERY := '';
        V_STR_QUERY := V_STR_QUERY || 'DROP TABLE IF EXISTS ' || V_TABLEINSERT1 || ' ';
        EXECUTE (V_STR_QUERY);

        V_STR_QUERY := '';
        V_STR_QUERY := V_STR_QUERY || 'CREATE TABLE ' || V_TABLEINSERT1 || ' AS SELECT * FROM IFRS_MASTER_RESTRU_SIFAT WHERE 1=0 ';
        EXECUTE (V_STR_QUERY);

        V_STR_QUERY := '';
        V_STR_QUERY := V_STR_QUERY || 'DROP TABLE IF EXISTS ' || V_TABLEINSERT2 || ' ';
        EXECUTE (V_STR_QUERY);

        V_STR_QUERY := '';
        V_STR_QUERY := V_STR_QUERY || 'CREATE TABLE ' || V_TABLEINSERT2 || ' AS SELECT * FROM IFRS_MASTER_RESTRU_COLLECT WHERE 1=0 ';
        EXECUTE (V_STR_QUERY);
    END IF;
    -------- ====== PRE SIMULATION TABLE ======

    -------- ====== BODY ======
    V_STR_QUERY := '';
    V_STR_QUERY := V_STR_QUERY || 'DELETE FROM ' || V_TABLEINSERT1 || ' 
        WHERE DOWNLOAD_DATE = ''' || CAST(V_CURRDATE AS VARCHAR(10)) || '''::DATE  ';
    EXECUTE (V_STR_QUERY);

    V_STR_QUERY := '';
    V_STR_QUERY := V_STR_QUERY || 'DELETE FROM ' || V_TABLEINSERT2 || ' 
        WHERE DOWNLOAD_DATE = ''' || CAST(V_CURRDATE AS VARCHAR(10)) || '''::DATE  ';
    EXECUTE (V_STR_QUERY);

    V_STR_QUERY := '';
    V_STR_QUERY := V_STR_QUERY || 'INSERT INTO ' || V_TABLEINSERT1 || ' 
        (    
            DOWNLOAD_DATE 
            ,RESTRUCTURE_DATE 
            ,PREVIOUS_MASTERID 
            ,MASTERID 
            ,PREVIOUS_ACCOUNT_NUMBER 
            ,ACCOUNT_NUMBER 
            ,CUSTOMER_NUMBER 
            ,CUSTOMER_NAME 
            ,BRANCH_CODE 
            ,PRODUCT_CODE 
            ,TGL_CAIR_BARU 
        ) SELECT 
            F_EOMONTH(CAST(TGL_MULAI_RESTRUK AS DATE), 0, ''M'', ''NEXT'') AS DOWNLOAD_DATE 
            ,TGL_MULAI_RESTRUK AS RESTRUCTURE_DATE 
            ,CONCAT(TRIM(CIF), ''_'', TRIM(OLD_DEAL_REF), ''_'', TRIM(DEAL_TYPE)) AS PREVIOUS_MASTERID 
            ,CONCAT(TRIM(CIF), ''_'', TRIM(DEAL_REF), ''_'', TRIM(DEAL_TYPE)) AS MASTERID 
            ,TRIM(OLD_DEAL_REF) AS PREVIOUS_ACCOUNT_NUMBER 
            ,TRIM(DEAL_REF) AS ACCOUNT_NUMBER 
            ,TRIM(CIF) AS CUSTOMER_NUMBER 
            ,TRIM(CUST_NAME) AS CUSTOMER_NAME 
            ,BRANCH AS BRANCH_CODE 
            ,TRIM(DEAL_TYPE) AS PRODUCT_CODE 
            ,TGL_CAIR_BARU 
        FROM DBLINK(''ifrs9_stg'', ''SELECT * FROM STG_LOAN_RESTRUKTUR_SIFAT'') 
        AS A ( 
            BUSS_DATE TIMESTAMP 
            ,CIF VARCHAR(10) 
            ,OLD_DEAL_REF VARCHAR(13) 
            ,DEAL_REF VARCHAR(13) 
            ,CUST_NAME VARCHAR(50) 
            ,BRANCH VARCHAR(4) 
            ,DEAL_TYPE VARCHAR(3) 
            ,OLD_COLLECT VARCHAR(1) 
            ,ORI_COLLECT VARCHAR(1) 
            ,COLLECT VARCHAR(1) 
            ,TGL_CAIR_BARU TIMESTAMP 
            ,TGL_MULAI_RESTRUK TIMESTAMP 
            ,TGL_SD TIMESTAMP 
            ,SPK_AWAL VARCHAR(25) 
            ,TGL_CAIR_AWAL TIMESTAMP 
            ,RESTRUKTUR_KE NUMERIC(3,0) 
            ,CARA_RESTRUKTUR VARCHAR(2) 
            ,FLAG_RESTRU_COVID19 VARCHAR(2) 
            ,SPK_AKHIR VARCHAR(25) 
            ,TGL_CAIR_AKHIR TIMESTAMP 
        ) WHERE BUSS_DATE = ''' || CAST(V_CURRDATE AS VARCHAR(10)) || '''::DATE 
        AND F_EOMONTH(CAST(TGL_MULAI_RESTRUK AS DATE), 0, ''M'', ''NEXT'') = ''' || CAST(V_CURRDATE AS VARCHAR(10)) || '''::DATE 
        AND CONCAT(TRIM(OLD_DEAL_REF), TRIM(CIF)) NOT IN (
            SELECT DISTINCT KEY_RESTRU FROM (
                SELECT CONCAT(TRIM(PREVIOUS_ACCOUNT_NUMBER), TRIM(CUSTOMER_NUMBER)) AS KEY_RESTRU 
                FROM ' || V_TABLEINSERT1 || ' 
                WHERE DOWNLOAD_DATE < ''' || CAST(V_CURRDATE AS VARCHAR(10)) || '''::DATE 
                GROUP BY PREVIOUS_ACCOUNT_NUMBER, CUSTOMER_NUMBER 
            ) A
        ) ';
    EXECUTE (V_STR_QUERY);

    GET DIAGNOSTICS V_RETURNROWS = ROW_COUNT;
    V_RETURNROWS2 := V_RETURNROWS2 + V_RETURNROWS;
    V_RETURNROWS := 0;

    V_STR_QUERY := '';
    V_STR_QUERY := V_STR_QUERY || 'INSERT INTO ' || V_TABLEINSERT1 || ' 
        (
            DOWNLOAD_DATE 
            ,RESTRUCTURE_DATE 
            ,PREVIOUS_MASTERID 
            ,MASTERID 
            ,PREVIOUS_ACCOUNT_NUMBER 
            ,ACCOUNT_NUMBER 
            ,CUSTOMER_NUMBER 
            ,CUSTOMER_NAME 
            ,BRANCH_CODE 
            ,PRODUCT_CODE 
            ,TGL_CAIR_BARU 
        ) SELECT 
            F_EOMONTH(CAST(BUSS_DATE AS DATE), 0, ''M'', ''NEXT'') AS DOWNLOAD_DATE 
            ,TGL_MULAI_RESTRUK AS RESTRUCTURE_DATE 
            ,CONCAT(TRIM(CIF), ''_'', TRIM(OLD_DEAL_REF), ''_'', TRIM(DEAL_TYPE)) AS PREVIOUS_MASTERID 
            ,CONCAT(TRIM(CIF), ''_'', TRIM(DEAL_REF), ''_'', TRIM(DEAL_TYPE)) AS MASTERID 
            ,TRIM(OLD_DEAL_REF) AS PREVIOUS_ACCOUNT_NUMBER 
            ,TRIM(DEAL_REF) AS ACCOUNT_NUMBER 
            ,TRIM(CIF) AS CUSTOMER_NUMBER 
            ,TRIM(CUST_NAME) AS CUSTOMER_NAME 
            ,BRANCH AS BRANCH_CODE 
            ,TRIM(DEAL_TYPE) AS PRODUCT_CODE 
            ,A.TGL_CAIR_BARU 
        FROM DBLINK(''ifrs9_stg'', ''SELECT * FROM STG_LOAN_RESTRUKTUR_SIFAT'') 
        AS A ( 
            BUSS_DATE TIMESTAMP 
            ,CIF VARCHAR(10) 
            ,OLD_DEAL_REF VARCHAR(13) 
            ,DEAL_REF VARCHAR(13) 
            ,CUST_NAME VARCHAR(50) 
            ,BRANCH VARCHAR(4) 
            ,DEAL_TYPE VARCHAR(3) 
            ,OLD_COLLECT VARCHAR(1) 
            ,ORI_COLLECT VARCHAR(1) 
            ,COLLECT VARCHAR(1) 
            ,TGL_CAIR_BARU TIMESTAMP 
            ,TGL_MULAI_RESTRUK TIMESTAMP 
            ,TGL_SD TIMESTAMP 
            ,SPK_AWAL VARCHAR(25) 
            ,TGL_CAIR_AWAL TIMESTAMP 
            ,RESTRUKTUR_KE NUMERIC(3,0) 
            ,CARA_RESTRUKTUR VARCHAR(2) 
            ,FLAG_RESTRU_COVID19 VARCHAR(2) 
            ,SPK_AKHIR VARCHAR(25) 
            ,TGL_CAIR_AKHIR TIMESTAMP 
        ) WHERE BUSS_DATE = ''' || CAST(V_CURRDATE AS VARCHAR(10)) || '''::DATE 
        AND CONCAT(TRIM(A.DEAL_REF), TRIM(A.CIF)) NOT IN (
            SELECT DISTINCT KEY_RESTRU FROM (
                SELECT CONCAT(TRIM(ACCOUNT_NUMBER), TRIM(CUSTOMER_NUMBER)) AS KEY_RESTRU 
                FROM ' || V_TABLEINSERT1 || ' 
                WHERE DOWNLOAD_DATE <= ''' || CAST(V_CURRDATE AS VARCHAR(10)) || '''::DATE 
                GROUP BY ACCOUNT_NUMBER, CUSTOMER_NUMBER 
            ) A
        ) ';
    EXECUTE (V_STR_QUERY);

    GET DIAGNOSTICS V_RETURNROWS = ROW_COUNT;
    V_RETURNROWS2 := V_RETURNROWS2 + V_RETURNROWS;
    V_RETURNROWS := 0;

    V_STR_QUERY := '';
    V_STR_QUERY := V_STR_QUERY || 'INSERT INTO ' || V_TABLEINSERT2 || ' 
        (
            DOWNLOAD_DATE 
            ,RESTRUCTURE_DATE 
            ,PREVIOUS_MASTERID 
            ,MASTERID 
            ,PREVIOUS_ACCOUNT_NUMBER 
            ,ACCOUNT_NUMBER 
            ,CUSTOMER_NUMBER 
            ,CUSTOMER_NAME 
            ,BRANCH_CODE 
            ,PRODUCT_CODE 
            ,TGL_CAIR_BARU 
        ) SELECT 
            BUSS_DATE AS DOWNLOAD_DATE 
            ,TGL_MULAI_RESTRUK AS RESTRUCTURE_DATE 
            ,CONCAT(TRIM(CIF), ''_'', TRIM(OLD_DEAL_REF), ''_'', TRIM(DEAL_TYPE)) AS PREVIOUS_MASTERID 
            ,CONCAT(TRIM(CIF), ''_'', TRIM(DEAL_REF), ''_'', TRIM(DEAL_TYPE)) AS MASTERID 
            ,TRIM(OLD_DEAL_REF) AS PREVIOUS_ACCOUNT_NUMBER 
            ,TRIM(DEAL_REF) AS ACCOUNT_NUMBER 
            ,TRIM(CIF) AS CUSTOMER_NUMBER 
            ,TRIM(CUST_NAME) AS CUSTOMER_NAME 
            ,BRANCH AS BRANCH_CODE 
            ,TRIM(DEAL_TYPE) AS PRODUCT_CODE 
            ,TGL_CAIR_BARU 
        FROM DBLINK(''ifrs9_stg'', ''SELECT * FROM STG_M_LOAN_RESTRUKTUR_COLLECT'') 
        AS A (
            BUSS_DATE TIMESTAMP 
            ,CIF VARCHAR(10) 
            ,OLD_DEAL_REF VARCHAR(13) 
            ,DEAL_REF VARCHAR(13) 
            ,CUST_NAME VARCHAR(50) 
            ,BRANCH VARCHAR(4) 
            ,DEAL_TYPE VARCHAR(3) 
            ,OLD_COLLECT VARCHAR(1) 
            ,ORI_COLLECT VARCHAR(1) 
            ,COLLECT VARCHAR(1) 
            ,TGL_CAIR_BARU TIMESTAMP 
            ,TGL_MULAI_RESTRUK TIMESTAMP 
            ,TGL_SD TIMESTAMP 
            ,SUFFIX VARCHAR(3) 
            ,ORI_PASTDUE_DAYS NUMERIC(5,0) 
            ,ORI_PASTDUE_DAYS_I NUMERIC(5,0) 
            ,ORI_INT_ACCRUED NUMERIC(18,0) 
            ,DPD NUMERIC(5,0) 
        ) WHERE BUSS_DATE = ''' || CAST(V_CURRDATE AS VARCHAR(10)) || '''::DATE ';
    EXECUTE (V_STR_QUERY);
    
    RAISE NOTICE 'SP_FETCH_IFRS_MASTER_RESTRU | AFFECTED RECORD : %', V_RETURNROWS2;
    ---------- ====== BODY ======

    -------- ====== LOG ======
    V_TABLEDEST = V_TABLEINSERT1;
    V_COLUMNDEST = '-';
    V_SPNAME = 'SP_FETCH_IFRS_MASTER_RESTRU';
    V_OPERATION = 'INSERT';
    
    CALL SP_IFRS_EXEC_AND_LOG(V_CURRDATE, V_TABLEDEST, V_COLUMNDEST, V_SPNAME, V_OPERATION, V_RETURNROWS2, P_RUNID);
    -------- ====== LOG ======

    -------- ====== RESULT ======
    V_QUERYS = 'SELECT * FROM ' || V_TABLEINSERT1 || '';
    CALL SP_IFRS_RESULT_PREV(V_CURRDATE, V_QUERYS, V_SPNAME, V_RETURNROWS2, P_RUNID);
    -------- ====== RESULT ======

END;

$$;