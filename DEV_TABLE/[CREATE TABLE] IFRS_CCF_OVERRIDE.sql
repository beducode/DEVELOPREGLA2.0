DROP TABLE IF EXISTS IFRS_CCF_OVERRIDE;

CREATE TABLE IF NOT EXISTS IFRS_CCF_OVERRIDE (
  PKID SERIAL PRIMARY KEY,
  CCF_CONFIGURATION VARCHAR(250) NOT NULL,
  SEGMENTATION_ID INT NOT NULL,
  SEGMENTATION VARCHAR(100) NOT NULL,
  DOWNLOAD_DATE DATE NOT NULL,
  CCF_RATE REAL NOT NULL,
  CCF_OVERRIDE REAL NOT NULL,
  CREATED_DATE TIMESTAMP WITHOUT TIME ZONE,
  UPDATED_DATE TIMESTAMP WITHOUT TIME ZONE
)