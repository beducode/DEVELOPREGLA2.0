DROP TABLE IF EXISTS IFRS_PD_FL_OVERRIDE;

CREATE TABLE IF NOT EXISTS IFRS_PD_FL_OVERRIDE (
	PKID SERIAL PRIMARY KEY,
	EFFECTIVE_DATE DATE NOT NULL,
	FL_DATE DATE NOT NULL,
	PD_CONFIGURATION_NAME VARCHAR(250) NOT NULL,
	BUCKET_GROUP VARCHAR(30) NOT NULL,
	FL_SEQ INT NOT NULL,
	FL_YEAR INT NOT NULL,
	FL_MONTH INT NOT NULL,
	PD_RATE_SYSTEM REAL NULL,
	PD_OVERRIDE REAL NULL,
	CREATED_DATE TIMESTAMP WITHOUT TIME ZONE,
	UPDATED_DATE TIMESTAMP WITHOUT TIME ZONE
)