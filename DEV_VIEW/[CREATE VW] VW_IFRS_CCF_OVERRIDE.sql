DROP VIEW IF EXISTS VW_CCF_OVERRIDE;

CREATE VIEW VW_CCF_OVERRIDE
AS
SELECT DISTINCT B.PKID, B.CCF_RULE_NAME AS "ccf_configuration",
B.SEGMENTATION_ID AS "segmentation_id", 
A.SEGMENT AS "segmentation", 
A.DOWNLOAD_DATE, 
A.CCF AS "ccf_rate", 
B.CCF_OVERRIDE AS "ccf_override"
FROM IFRS_MASTER_ACCOUNT A
INNER JOIN IFRS_CCF_RULES_CONFIG B ON A.SEGMENTATION_ID = B.SEGMENTATION_ID
WHERE A.DOWNLOAD_DATE = (SELECT CURRDATE FROM IFRS_PRC_DATE)
AND B.IS_DELETE = 0 AND B.ACTIVE_FLAG = 1;